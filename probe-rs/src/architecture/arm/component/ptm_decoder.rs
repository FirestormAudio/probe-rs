//! Decoder for the PTM (Program Trace Macrocell) packet stream.
//!
//! Implements the ETMv3 PFT packet format as specified in ARM IHI0035B §4
//! (Program Flow Trace Protocol). Consumes raw bytes captured from an ETF
//! circular buffer and produces a sequence of [`PtmPacket`] values.
//!
//! ## Packet identification summary (IHI0035B Table 4.1)
//!
//! | Header byte (binary) | Value  | Packet type                       |
//! |----------------------|--------|-----------------------------------|
//! | `0000_0000`          | 0x00   | A-Sync (≥5 × 0x00 then 0x80)     |
//! | `0000_1000`          | 0x08   | I-Sync (instruction synchronization) |
//! | `0000_1100`          | 0x0C   | Trigger                           |
//! | `0111_0010`          | 0x72   | Waypoint update (+ 1-5 addr bytes)|
//! | `0111_0110`          | 0x76   | Exception return (PFTv1.1 / A only)|
//! | `0110_0110`          | 0x66   | Ignore                            |
//! | `0110_1110`          | 0x6E   | Context ID                        |
//! | `0011_1100`          | 0x3C   | VMID                              |
//! | `0100_0x10`          | 0x42/0x46 | Timestamp                      |
//! | `xxxx_xxx1` (odd)    | —      | Branch address                    |
//! | `1xxx_xxx0` (even)   | —      | Atom (P-header); Formats 1-5      |
//!
//! ## Limitations
//!
//! - Does not decode VMID, timestamp, or ContextID payload bytes (they are consumed and
//!   represented as opaque variants).
//! - Does not reconstruct full instruction traces (that requires the ELF binary).
//! - Input must start at a synchronization point (A-Sync + I-Sync sequence). Leading
//!   bytes before the first sync are discarded.
//!
//! ## Usage
//!
//! ```
//! use probe_rs::architecture::arm::component::ptm_decoder::{Decoder, PtmPacket};
//!
//! let raw_ptm_bytes: &[u8] = &[/* ... */];
//! // context_id_bytes = 0 for bare-metal targets (no OS context IDs).
//! let decoder = Decoder::new(raw_ptm_bytes, 0);
//! for packet in decoder {
//!     println!("{packet}");
//! }
//! ```

use std::fmt;

/// A decoded PTM packet.
#[derive(Debug, Clone, PartialEq, serde::Serialize)]
#[serde(tag = "type", content = "data")]
pub enum PtmPacket {
    /// Synchronization sequence complete (`0x00 × 5..N, 0x80`).
    Sync,

    /// I-Sync (instruction address sync) packet.
    ///
    /// Provides the current PC and processor state at a sync / exception boundary.
    ISync {
        /// Program Counter value (bits [31:1], bit 0 encodes Thumb state).
        pc: u32,
        /// CPSR (only the low 8 bits are typically traced: mode + T + F + I bits).
        cpsr_low8: u8,
        /// Context ID bytes (0–4 bytes depending on ETMCR.CONTEXTIDSIZE).
        context_id: u32,
    },

    /// Branch address packet (direct or indirect branch target, partially or fully qualified).
    ///
    /// The `address` field is the complete reconstructed branch target. `is_exception`
    /// is set when this is an exception entry branch. `is_link` is set when the branch
    /// is a function-call with link (BL/BLX).
    Branch {
        /// Reconstructed branch target address.
        address: u32,
        /// Set when this branch is an exception entry (e.g. IRQ, Prefetch Abort).
        is_exception: bool,
        /// Set when the branch is a function call with link (BL/BLX).
        is_link: bool,
        /// Non-secure bit from the I-Sync context before this branch.
        ns: bool,
    },

    /// P-Header (atom) packet: a run of Executed (E) / Not-executed (N) outcomes
    /// for direct conditional branches.
    Atom {
        /// True bits are Executed (branch taken), false bits are Not-executed.
        /// Bit 0 is the first branch in order.
        executed: u8,
        /// Number of valid low bits in `executed`. Always 1–6.
        count: u8,
    },

    /// Trigger packet — an ETM trigger event was signalled at this point in the trace.
    Trigger,

    /// Waypoint update — the most recent non-waypoint instruction was upgraded
    /// to a waypoint to allow full reconstruction across an exception.
    WaypointUpdate,

    /// Exception return (PFTv1.1, ARMv7-A/R only — never generated by PFTv1.0).
    ExceptionReturn,

    /// Overflow — some trace bytes were lost.
    Overflow,

    /// Context ID update (raw, un-parsed payload).
    ContextId(u32),

    /// Timestamp (raw, un-parsed payload).
    Timestamp(u64),

    /// An unknown or malformed packet header — the decoder skipped this byte.
    Unknown(u8),
}

impl fmt::Display for PtmPacket {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Sync => write!(f, "SYNC"),
            Self::ISync { pc, cpsr_low8, context_id } => write!(
                f,
                "ISYNC  pc={pc:#010x}  cpsr_low8={cpsr_low8:#04x}  ctx={context_id:#010x}"
            ),
            Self::Branch { address, is_exception, is_link, ns } => {
                write!(f, "BRANCH {address:#010x}")?;
                if *is_link { write!(f, " LINK")?; }
                if *is_exception { write!(f, " EXCEPTION")?; }
                if *ns { write!(f, " NS")?; }
                Ok(())
            }
            Self::Atom { executed, count } => {
                write!(f, "ATOM   ")?;
                for i in 0..*count {
                    let ch = if (executed >> i) & 1 == 1 { 'E' } else { 'N' };
                    write!(f, "{ch}")?;
                }
                Ok(())
            }
            Self::Trigger => write!(f, "TRIGGER"),
            Self::WaypointUpdate => write!(f, "WAYPOINT"),
            Self::ExceptionReturn => write!(f, "EXCEPTION_RETURN"),
            Self::Overflow => write!(f, "OVERFLOW"),
            Self::ContextId(id) => write!(f, "CONTEXT_ID {id:#010x}"),
            Self::Timestamp(ts) => write!(f, "TIMESTAMP {ts}"),
            Self::Unknown(b) => write!(f, "UNKNOWN  {b:#04x}"),
        }
    }
}

