    // ── Atoms (all 5 formats) ────────────────────────────────────────────────
    //
    // IHI0035B Table 4.3: atom header = `1xxx_xxx0` (bit7=1, bit0=0).
    // The highest set bit among bits[6:2] gives the format (count):
    //   bit6 → Format 5 (5 atoms at bits[5:1])
    //   bit5 → Format 4 (4 atoms at bits[4:1])
    //   bit4 → Format 3 (3 atoms at bits[3:1])
    //   bit3 → Format 2 (2 atoms at bits[2:1])
    //   bit2 → Format 1 (1 atom at bit1)
    //
    // F=0 in each bit position means E (Executed/taken); F=1 means N (Not-executed).
    // Atom N=0 is oldest; in `PtmPacket::Atom` bit N of `executed` is 1 when atom N is E.

