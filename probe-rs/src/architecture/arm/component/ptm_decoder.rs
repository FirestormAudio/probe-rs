    // ── Branch address ───────────────────────────────────────────────────────

    /// 2-byte branch with no exception: [0x81, 0x01] → address 0x80.
    ///
    /// byte[0]=0x81: bit[7]=1 (continuation), addr = (0x81 & 0x7E)>>1 = 0.
    /// byte[1]=0x01: bit[7]=0 (final), bit[6]=0 (no exception), bits[4:0]=0x01.
    ///   addr |= (0x01 & 0x1F) << 6 = 0x40.
    ///   addr_bits=11, mask=0x0FFE → address = (0 & ~0x0FFE) | (0x40<<1 & 0x0FFE) = 0x80.
    #[test]
    fn decode_branch_two_bytes() {
        let mut data = async_seq();
        data.extend([0x81, 0x01]);
        let pkts = decode_all(&data, 0);
        assert!(
            matches!(pkts[1], PtmPacket::Branch { address: 0x80, is_exception: false, .. }),
            "got {:?}",
            pkts[1]
        );
    }

    /// 2-byte branch with exception bit set: exception-info byte must be consumed
    /// and the next valid packet must decode without producing UNKNOWN.
    ///
    /// byte[0]=0xfd: continuation, addr = (0xfd & 0x7E)>>1 = 0x3E.
    /// byte[1]=0x43: final, bit[6]=1 (exception!), bits[4:0]=0x03.
    ///   addr |= 0x03 << 6 = 0xC0 → addr=0xFE.
    ///   address = 0 | (0xFE<<1 & 0xFFE) = 0x1FC.
    /// byte 0x1c: exception-info byte, S=0 → consumed, no Unknown emitted.
    /// byte 0x0C: Trigger → decoded correctly.
    #[test]
    fn decode_branch_two_bytes_with_exception_info() {
        let mut data = async_seq();
        data.extend([0xfd, 0x43, 0x1c, 0x0C]); // branch + exc-info + trigger
        let pkts = decode_all(&data, 0);
        assert_eq!(pkts.len(), 3, "expected Sync + Branch + Trigger, got {pkts:?}");
        assert!(
            matches!(pkts[1], PtmPacket::Branch { address: 0x1fc, is_exception: true, .. }),
            "expected exception branch at 0x1fc, got {:?}", pkts[1]
        );
        assert_eq!(pkts[2], PtmPacket::Trigger, "got {:?}", pkts[2]);
    }

    /// 2-byte branch with altISA only (bit[5]=1, bit[6]=0): altISA byte consumed,
    /// exception-info NOT consumed.
    ///
    /// final byte = 0x23 = 0010_0011: bit[6]=0, bit[5]=1 (altISA), bits[4:0]=0x03.
    /// addr |= 0x03 << 6 = 0xC0 → addr = 0x3E | 0xC0 = 0xFE → address = 0x1FC.
    /// 0xAB: altISA byte (consumed silently).
    /// 0x0C: Trigger → decoded correctly.
    #[test]
    fn decode_branch_two_bytes_with_alt_isa() {
        let mut data = async_seq();
        // byte[0]=0x9d = 10011101: bit7=1(cont), bits[6:1]=(0x9d&0x7E)>>1=(0x1C)>>1=0x0E=0b001110
        // addr = 0x0E
        // byte[1]=0x23 = 00100011: bit7=0(final), bit6=0(no exc), bit5=1(altISA), bits[4:0]=0x03
        //   addr |= 0x03 << 6 = 0xC0 → addr = 0x0E | 0xC0 = 0xCE
        //   address = (0 & ~0xFFE) | (0xCE<<1 & 0xFFE) = (0x19C & 0xFFE) = 0x19C
        data.extend([0x9d, 0x23, 0xAB, 0x0C]); // branch + altISA byte + trigger
        let pkts = decode_all(&data, 0);
        assert_eq!(pkts.len(), 3, "expected Sync + Branch + Trigger, got {pkts:?}");
        assert!(
            matches!(pkts[1], PtmPacket::Branch { is_exception: false, .. }),
            "unexpected exception flag, got {:?}", pkts[1]
        );
        assert_eq!(pkts[2], PtmPacket::Trigger, "altISA byte was not consumed, got {:?}", pkts[2]);
    }

    /// 2-byte branch with both altISA and exception set: altISA byte consumed first,
    /// then exception-info byte, leaving the next packet clean.
